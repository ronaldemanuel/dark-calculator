    // Verifica se o numero é decimal ou inteiro para a devida formatação
    public boolean inteiro(double num) {
        if (num % 1 == 0) {
            return true;
        } else {
            return false;
        }
    }
    
    // Força a String a ter um determinado tamanho
    public String limitaString(String str) {
        if (str.length() > 15) {
            return str.substring(0, 15);
        } else {
            return str;
        }
    } 

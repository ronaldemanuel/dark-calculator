    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        // Resultado
        num2 = mostrador.getText();
        c = new Calculos(Double.parseDouble(num1), Double.parseDouble(num2));
        mostradorSup.setText(mostradorSup.getText()+" "+num2+" =");
        switch (sinal) {
            case 0:
                resNum = c.soma();
                break;
            case 1:
                resNum = c.subtracao();
                break;
            case 2:
                resNum = c.mult();
                break;
            case 3:
                resNum = c.div();
                break;
            case 4:
                resNum = c.pot();
        }
        if (inteiro(resNum)) {
            int resInt = (int)resNum;
            resStr = Integer.toString(resInt);
        } else {
            resStr = Double.toString(resNum);
            resStr = limitaString(resStr);
        }
        mostrador.setText(resStr);
    }//GEN-LAST:event_jButton18ActionPerformed

